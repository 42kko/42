#include <unistd.h>

void	wr(char c)
{
	write(1, &c, 1);
}

void	rec(int n)
{
	char	a;

	if (n == 0)
		return ;
	rec(n / 10);
	a = 48 + (n % 10);
	wr(a);
}

void	ft_putnbr(int nb)
{
	if (nb == 0)
		write(1, "0", 1);
	if (nb == -2147483648)
	{
		wr('-');
		wr('2');
		rec(147483648);
	}
	else if (nb < 0)
	{
		wr('-');
		rec(-1 * nb);
	}
	else
	{
		rec(nb);
	}
}
