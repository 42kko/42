void	ft_rev_int_tab(int *tab, int size)
{
	int	a;
	int	i;

	i = 0;
	if (size != '\0')
	{
		while (i < size - 1)
		{
			a = tab[i];
			tab[i] = tab[size - 1];
			tab[size - 1] = a;
			size--;
			i++;
		}
	}
}
