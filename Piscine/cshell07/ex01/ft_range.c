#include <stdlib.h>

int	*ft_range(int min, int max)
{
	int	*a;
	int	i;

	if (min >= max)
		return (0);
	a = malloc(sizeof(int) * (max - min));
	i = 0;
	while (i < max - min)
	{
		a[i] = min + i;
		i++;
	}
	return (a);
}
