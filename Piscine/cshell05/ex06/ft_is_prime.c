int	ft_is_prime(int nb)
{
	long long	i;

	i = 2;
	if (nb == 1 || nb <= 0)
		return (0);
	while (i * i <= (long long)nb)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}
