#include <unistd.h>
#include "ft_stock_str.h"

void	ft_nbr(int nb)
{
	char	a;

	if (nb == 0)
		return ;
	a = '0' + nb % 10;
	ft_nbr(nb / 10);
	write (1, &a, 1);
}

void	ft_show_tab(struct s_stock_str *par)
{
	int	i;

	i = 0;
	while (par[i].str)
	{
		write(1, par[i].str, par[i].size);
		write(1, "\n", 1);
		if (par[i].size == 0)
			write(1, "0", 1);
		else
			ft_nbr(par[i].size);
		write(1, "\n", 1);
		write(1, par[i].copy, par[i].size);
		write(1, "\n", 1);
		i++;
	}
}
