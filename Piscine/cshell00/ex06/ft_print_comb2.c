#include <unistd.h>

void	wr(char c)
{
	write(1, &c, 1);
}

void	comb(int i)
{
	char	a;

	a = (i / 10) + 48;
	wr (a);
	a = (i % 10) + 48;
	wr (a);
}

void	ft_print_comb2(void)
{
	int		i;
	int		i1;

	i = 0;
	while (i <= 98)
	{
		i1 = 1 + i;
		while (i1 <= 99)
		{
			comb(i);
			wr(' ');
			comb(i1);
			if (!(i == 98 && i1 == 99))
			{	
				wr(',');
				wr(' ');
			}
			i1++;
		}
		i++;
	}
}
