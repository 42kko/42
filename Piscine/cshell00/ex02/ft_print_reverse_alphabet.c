#include <unistd.h>

void	wr(char c)
{
	write (1, &c, 1);
}

void	ft_print_reverse_alphabet(void)
{
	char	i;

	i = 0;
	while (i < 26)
	{
		wr (122 - i);
		i++;
	}
}
